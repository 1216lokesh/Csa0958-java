public class VariableExample {
    public static void main(String[] args) {
        // Boolean variable
        boolean result = true;

        // Character variable
        char capitalC = 'C';

        // Byte variable
        byte b = 100;

        // Short variable
        short s = 10000;

        // Integer variable
        int i = 100000;

        // Long variable with underscore for readability and 'L' suffix
        long creditCardNumber = 1234_5678_9012_3456L;

        // Printing all variables
        System.out.println("Boolean result: " + result);
        System.out.println("Character capitalC: " + capitalC);
        System.out.println("Byte b: " + b);
        System.out.println("Short s: " + s);
        System.out.println("Integer i: " + i);
        System.out.println("Long creditCardNumber: " + creditCardNumber);
    }
}